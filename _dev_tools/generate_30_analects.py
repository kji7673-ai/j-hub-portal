import random
colors = ["#0066cc", "#1d1d1f"]

analects_json = []

for item in raw_data:
    chars_split = item['chars'].split(", ")
    characters = []
    
    hanja_no_space = item['hanja'].replace(" ", "").replace(",", "")
    
    for c_str in chars_split:
        parts = c_str.split(":")
        char_h = parts[0].strip()
        reading = parts[1].strip()
        characters.append({
            "char": char_h,
            "reading": reading,
            "color": random.choice(colors)
        })
    
    # Actually, the hanja string in JSON requires all characters, including punctuation
    # Let's rebuild the characters array based on the hanja string
    # Not every character in the hanja string has a breakdown (like commas).
    # We will only provide the breakdown for the Hanja characters present in `chars_split`.
    
    char_dict = {p.split(":")[0].strip(): p.split(":")[1].strip() for p in chars_split}
    
    final_chars = []
    hanja_tokens = item['hanja'].replace(",", "").split(" ")
    for tok in hanja_tokens:
        if tok in char_dict:
            final_chars.append({
                "char": tok,
                "reading": char_dict[tok],
                "color": random.choice(colors)
            })
        elif tok.strip() != "":
            # Fallback if I missed one
            final_chars.append({
                "char": tok,
                "reading": "한자",
                "color": "#1d1d1f"
            })
            
    analects_json.append({
        "hanja": item['hanja'],
        "korean": item['korean'],
        "characters": final_chars,
        "application": item['application']
    })

with open('analects.json', 'w', encoding='utf-8') as f:
    json.dump(analects_json, f, ensure_ascii=False, indent=4)

